import Foundation

class MathematicFunc {
    
 func addInteger(x: Int, y: Int) -> Int {
        
        return x + y
        
    }
    func multiply(x: Int, y: Int) -> Int {
        
        return x * y
        
    }
    func divide(x: Int, y: Int) -> Int {
        
        return x / y
        
    }
    func minus(x: Int, y: Int) -> Int {
        
        return x - y
        
    }
    
}
